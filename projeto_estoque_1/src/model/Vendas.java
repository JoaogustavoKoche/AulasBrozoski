
package model;


public class Vendas {
 
 private  Integer id_v;
 private  Integer n_notav;
 private  Integer cod_mer;
 private  Integer cod_cli;
 private  String preco_venda;
 private  String referencia;
 private  String descricao;

    public Integer getId_v() {
        return id_v;
    }

    public void setId_v(Integer id_v) {
        this.id_v = id_v;
    }
 
    public Integer getN_notav() {
        return n_notav;
    }

    public void setN_notav(Integer n_notav) {
        this.n_notav = n_notav;
    }

    public Integer getCod_mer() {
        return cod_mer;
    }

    public void setCod_mer(Integer cod_mer) {
        this.cod_mer = cod_mer;
    }

    public Integer getCod_cli() {
        return cod_cli;
    }

    public void setCod_cli(Integer cod_cli) {
        this.cod_cli = cod_cli;
    }

    public String getPreco_venda() {
        return preco_venda;
    }

    public void setPreco_venda(String preco_venda) {
        this.preco_venda = preco_venda;
    }

    public String getReferencia() {
        return referencia;
    }

    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
    
    
  
  
    
}
